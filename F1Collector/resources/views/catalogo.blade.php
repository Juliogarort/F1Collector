Pastelería Julio García
Este proyecto corresponde a una aplicación web para gestionar una pastelería, desarrollada en PHP con integración de bases de datos y funcionalidades de usuario. A continuación, se detalla la estructura y funcionalidades principales de la aplicación.

Funcionalidades principales
Usuarios
Registro y Login: Los usuarios pueden registrarse y acceder a su cuenta a través de las páginas registro.php y login.php.
Gestión de sesión: La sesión de usuario es gestionada utilizando cookies y sesiones.

Gestión de productos
Clases orientadas a objetos:
Dulce.php: Clase base para representar dulces.
Tarta.php, Bollo.php y Chocolate.php: Clases que heredan de Dulce para representar tipos específicos de productos.
Cliente.php: Clase para gestionar la información de los clientes.
Pasteleria.php: Clase principal que gestiona el inventario y las compras de la pastelería.
Excepciones personalizadas: Manejo de errores mediante excepciones específicas como:
ClienteNoEncontradoException
DulceNoCompradoException
DulceNoEncontradoException
DulceYaCompradoException
Administrador
Panel de administración: mainAdmin.php permite a los administradores gestionar productos, visualizar clientes y realizar acciones específicas sobre el sistema.

Página principal
Visualización de productos: Los usuarios pueden explorar los productos disponibles en la página principal main.php.

Base de datos
Estructura: El archivo Pasteleria.sql contiene la definición de la base de datos, incluyendo tablas para productos, clientes y registros de compras.
Conexión: La clase Conexion.php se encarga de gestionar la conexión a la base de datos mediante PDO.

Estructura de carpetas
public/css: Contiene los estilos CSS para la aplicación.
public/img: Imágenes de productos y logotipos.
public/js: Scripts de JavaScript para mejorar la interactividad.
public/src: Clases principales que implementan la lógica del negocio.
public/util: Clases auxiliares como excepciones y la conexión a la base de datos.

Instalación y ejecución
Clonar el repositorio o descomprimir el archivo ZIP.
Importar el archivo Pasteleria.sql a un gestor de bases de datos MySQL.
Configurar el archivo Conexion.php con las credenciales correctas de la base de datos.
Asegurarse de que el servidor Apache esté configurado y que el módulo de PHP esté activo.
Acceder a la aplicación a través de un navegador web.

Requisitos
PHP 7.4 o superior.
Servidor Apache.
MySQL.

Notas adicionales
Este proyecto incluye un archivo .git para la gestión del control de versiones.
La aplicación utiliza un enfoque modular para facilitar su mantenimiento y escalabilidad.
